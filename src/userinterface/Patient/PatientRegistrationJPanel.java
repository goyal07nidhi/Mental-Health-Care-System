    private void uploadJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_uploadJBtnActionPerformed

        JFileChooser filechooser = new JFileChooser();
        filechooser.setDialogTitle("Upload Photo");

        int returnval = filechooser.showOpenDialog(this);
        if (returnval == JFileChooser.APPROVE_OPTION)
        {
            File file = filechooser.getSelectedFile();
            try {
                this.profileImage = ImageIO.read(file);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Unable to upload photo", "Error Message", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_uploadJBtnActionPerformed

