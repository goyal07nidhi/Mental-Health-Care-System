    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        String userName = this.unameTxtField.getText();
        String emailId = this.emailTxtField.getText();
        String password = String.valueOf(this.pwdTxtField.getPassword());
        String name = nameTxtField.getText();
        
        if (!isUserNameValid()) {
            JOptionPane.showMessageDialog(null, "Enter a valid username", "Error", JOptionPane.ERROR_MESSAGE);
            return;            
        }
        
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter valid name", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!isPasswordValid()) {
            JOptionPane.showMessageDialog(null, "Password should be at least 6 digits and contain at least one upper case letter, one lower case letter, one digit and one special character", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!system.isUserNameUnique(userName)) {
            JOptionPane.showMessageDialog(null, "Username is already taken", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (emailId.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter valid emailId", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        float height;
        try {
            height = Float.parseFloat(htTxtField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid Height", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        float weight;
        try {
            weight = Float.parseFloat(wtTxtField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid Weight", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        float bmi;
        try {
            bmi = Float.parseFloat(bmiTxtField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid BMI", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int age;
        try {
            age = Integer.parseInt(agetxtField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid age", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Employee employee = new Employee(this.unameTxtField.getText());
        
        Patient p = new Patient();
        p.setName(name);
        p.setAge(age);
        p.setGender((String)genderCombo.getSelectedItem());
        p.setLocation((String)networkCombo.getSelectedItem());
        p.setHeight(height);
        p.setWeight(weight);
        p.setBmi(bmi);
        p.setEmailId(emailId);
        
        if (this.profileImage != null) {
            p.setProfileImage(this.profileImage);
        }
        
        system.getUserAccountDirectory().createUserAccount(userName, password, employee, p);
        JOptionPane.showMessageDialog(null, "Profile successfully created. Login using the credentials", "Info Message", JOptionPane.INFORMATION_MESSAGE);
        dB4OUtil.storeSystem(system);
        
        userProcessContainer.remove(this);

        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_CreateBtnActionPerformed

