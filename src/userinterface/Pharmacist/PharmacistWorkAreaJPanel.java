    private void populatePharmacyRequestTable() {
        DefaultTableModel model = (DefaultTableModel) pharmacyTbl.getModel();
        model.setRowCount(0);
        
        if (phmcyOrganization.getWorkQueue() == null) {
            System.out.println("WorkQueue is NULL for " + phmcyOrganization.getName());
            return;
        }

        for (WorkRequest request : phmcyOrganization.getWorkQueue().getWorkRequestList()) {
            if (request == null) {
                continue;
            }

            // Do not show requests that are assigned to other pharmacists
            if (request.getReceiver() != null && !request.getReceiver().equals(userAccount)) {
                continue;
            }

            PharmacyWorkRequest labRequest = (PharmacyWorkRequest)request;
            Object[] row = new Object[4];
            row[0] = labRequest.getPatient().getName();
            row[1] = labRequest.getSender().getEmployee().getName();
            row[2] = labRequest.getMessage();
            row[3] = labRequest;

            model.addRow(row);
        }
    }

