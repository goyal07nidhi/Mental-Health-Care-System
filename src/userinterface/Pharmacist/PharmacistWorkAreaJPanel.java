    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        int row = pharmacyTbl.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Select a medicine request", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        PharmacyWorkRequest request = (PharmacyWorkRequest)pharmacyTbl.getValueAt(row, 3);
        request.setReceiver(userAccount);
        request.setStatus("Acknowledged");
        populatePharmacyRequestTable();
    }//GEN-LAST:event_assignBtnActionPerformed

    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        int row = pharmacyTbl.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Select a medicine request", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        PharmacyWorkRequest request = (PharmacyWorkRequest)pharmacyTbl.getValueAt(row, 3);
        
        if (request.getReceiver() == null) {
            JOptionPane.showMessageDialog(null, "You need to assign the request to yourself before processing", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        request.setStatus("Processing");
        populatePharmacyRequestTable();
    }//GEN-LAST:event_processBtnActionPerformed

    private void readyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_readyBtnActionPerformed
        int row = pharmacyTbl.getSelectedRow();

        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Select a medicine request", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }

        PharmacyWorkRequest request = (PharmacyWorkRequest)pharmacyTbl.getValueAt(row, 3);
        if (request.getReceiver() == null) {
            JOptionPane.showMessageDialog(null, "You need to assign the request to yourself before processing", "Error Message", JOptionPane.ERROR_MESSAGE);
            return;
        }

        request.setStatus("Ready For Pickup");
        request.setResolveDate(new Date());
        populatePharmacyRequestTable();
    }//GEN-LAST:event_readyBtnActionPerformed


